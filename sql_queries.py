SELECT_QUERIES = """
-- ========================================
-- SELECT: Get all students with hostel and department
-- Demonstrates: INNER JOIN / LEFT JOIN
-- ========================================
SELECT 
    s.student_id,
    s.name,
    s.email,
    s.roll_number,
    h.hostel_name,
    d.department_name,
    d.department_code
FROM students s
LEFT JOIN hostels h ON s.hostel_id = h.hostel_id
LEFT JOIN departments d ON s.department_id = d.department_id
ORDER BY s.name;

-- ========================================
-- SELECT: Get order history for a student
-- Demonstrates: JOIN, GROUP BY, aggregate functions
-- ========================================
SELECT 
    o.order_id,
    o.order_date,
    o.order_status,
    o.order_type,
    o.total_amount,
    GROUP_CONCAT(mi.item_name SEPARATOR ', ') as items,
    p.payment_status
FROM orders o
LEFT JOIN order_items oi ON o.order_id = oi.order_id
LEFT JOIN menu_items mi ON oi.item_id = mi.item_id
LEFT JOIN payments p ON o.order_id = p.order_id
WHERE o.student_id = 1
GROUP BY o.order_id
ORDER BY o.order_date DESC;

-- ========================================
-- SELECT: Get menu items with filters
-- Demonstrates: WHERE with multiple conditions
-- ========================================
SELECT 
    item_id,
    item_name,
    price,
    category,
    is_vegetarian,
    is_available
FROM menu_items
WHERE canteen_id = 1
  AND is_available = TRUE
  AND category = 'Breakfast'
ORDER BY item_name;

-- ========================================
-- SELECT: Get orders by status (for kitchen)
-- Demonstrates: Enum handling, JOIN
-- ========================================
SELECT 
    o.order_id,
    o.order_date,
    s.name as student_name,
    s.roll_number,
    o.order_type,
    o.special_instructions
FROM orders o
JOIN students s ON o.student_id = s.student_id
WHERE o.order_status = 'PLACED'
ORDER BY o.order_date ASC;

-- ========================================
-- SELECT: Get full order details
-- Demonstrates: Multiple JOINs
-- ========================================
SELECT 
    o.order_id,
    o.order_date,
    o.order_status,
    o.order_type,
    o.total_amount,
    s.name as student_name,
    s.roll_number,
    mi.item_name,
    oi.quantity,
    oi.unit_price,
    oi.subtotal,
    p.payment_method,
    p.payment_status,
    dp.delivery_address,
    dp.is_delivered,
    dperson.name as delivery_person
FROM orders o
JOIN students s ON o.student_id = s.student_id
LEFT JOIN order_items oi ON o.order_id = oi.order_id
LEFT JOIN menu_items mi ON oi.item_id = mi.item_id
LEFT JOIN payments p ON o.order_id = p.order_id
LEFT JOIN delivery_pickups dp ON o.order_id = dp.order_id
LEFT JOIN delivery_personnel dperson ON dp.personnel_id = dperson.personnel_id
WHERE o.order_id = 1;
"""


ADVANCED_QUERIES = """
-- ========================================
-- SUBQUERY: Students who have never ordered
-- ========================================
SELECT s.student_id, s.name, s.email
FROM students s
WHERE s.student_id NOT IN (
    SELECT DISTINCT student_id FROM orders
);

-- ========================================
-- SUBQUERY: Items never ordered
-- ========================================
SELECT mi.item_id, mi.item_name
FROM menu_items mi
WHERE mi.item_id NOT IN (
    SELECT DISTINCT item_id FROM order_items
);

-- ========================================
-- HAVING: Departments with more than 2 students
-- ========================================
SELECT 
    d.department_code,
    d.department_name,
    COUNT(s.student_id) as student_count
FROM departments d
JOIN students s ON d.department_id = s.department_id
GROUP BY d.department_id
HAVING COUNT(s.student_id) > 2;

-- ========================================
-- SELF-JOIN: Find students in same hostel
-- ========================================
SELECT 
    s1.name as student1,
    s2.name as student2,
    h.hostel_name
FROM students s1
JOIN students s2 ON s1.hostel_id = s2.hostel_id AND s1.student_id < s2.student_id
JOIN hostels h ON s1.hostel_id = h.hostel_id;

-- ========================================
-- UNION: All orders (pickup and delivery) with type
-- ========================================
SELECT order_id, 'PICKUP' as type, NULL as delivery_address
FROM orders WHERE order_type = 'PICKUP'
UNION
SELECT o.order_id, 'DELIVERY' as type, dp.delivery_address
FROM orders o
JOIN delivery_pickups dp ON o.order_id = dp.order_id
WHERE o.order_type = 'DELIVERY';

-- ========================================
-- VIEW: Create a view for order summary
-- ========================================
CREATE VIEW order_summary AS
SELECT 
    o.order_id,
    s.name as student_name,
    o.order_date,
    o.order_status,
    o.total_amount,
    p.payment_status
FROM orders o
JOIN students s ON o.student_id = s.student_id
LEFT JOIN payments p ON o.order_id = p.order_id;

-- Usage:
SELECT * FROM order_summary WHERE order_status = 'PLACED';
"""


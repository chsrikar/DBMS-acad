REPORT_QUERIES = """
-- ========================================
-- REPORT: Daily Revenue
-- Demonstrates: Aggregate functions, GROUP BY, CASE WHEN
-- ========================================
SELECT 
    DATE(o.order_date) as date,
    COUNT(DISTINCT o.order_id) as total_orders,
    COUNT(DISTINCT CASE WHEN p.payment_status = 'COMPLETED' THEN o.order_id END) as completed_orders,
    COALESCE(SUM(CASE WHEN p.payment_status = 'COMPLETED' THEN p.amount_paid END), 0) as total_revenue,
    COALESCE(SUM(CASE WHEN p.payment_status = 'COMPLETED' AND p.payment_method = 'CASH' 
                 THEN p.amount_paid END), 0) as cash_revenue,
    COALESCE(SUM(CASE WHEN p.payment_status = 'COMPLETED' AND p.payment_method = 'UPI' 
                 THEN p.amount_paid END), 0) as upi_revenue
FROM orders o
LEFT JOIN payments p ON o.order_id = p.order_id
WHERE DATE(o.order_date) = '2024-01-10'
GROUP BY DATE(o.order_date);

-- ========================================
-- REPORT: Most Popular Items
-- Demonstrates: Subquery, ORDER BY with aggregate
-- ========================================
SELECT 
    mi.item_id,
    mi.item_name,
    mi.price,
    COUNT(oi.order_item_id) as times_ordered,
    SUM(oi.quantity) as total_quantity,
    SUM(oi.subtotal) as total_revenue
FROM menu_items mi
LEFT JOIN order_items oi ON mi.item_id = oi.item_id
GROUP BY mi.item_id
ORDER BY times_ordered DESC
LIMIT 10;

-- ========================================
-- REPORT: Student Order Summary
-- Demonstrates: Aggregate with GROUP BY
-- ========================================
SELECT 
    s.student_id,
    s.name,
    s.roll_number,
    COUNT(o.order_id) as total_orders,
    COALESCE(SUM(o.total_amount), 0) as total_spent
FROM students s
LEFT JOIN orders o ON s.student_id = o.student_id
GROUP BY s.student_id
ORDER BY total_spent DESC;

-- ========================================
-- REPORT: Category-wise Sales
-- Demonstrates: GROUP BY with JOIN
-- ========================================
SELECT 
    mi.category,
    COUNT(DISTINCT oi.order_item_id) as items_sold,
    SUM(oi.subtotal) as category_revenue
FROM menu_items mi
JOIN order_items oi ON mi.item_id = oi.item_id
GROUP BY mi.category
ORDER BY category_revenue DESC;
"""

